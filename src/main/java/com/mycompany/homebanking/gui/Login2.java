    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        
        String nombreUsu = txtUsuario.getText();
        String contrasenia = txtPass.getText();
        
        Usuario usu = control.validarUsuario(nombreUsu, contrasenia);
        
        if( usu!=null ) {
            
            if(usu.getRol().equals("admin")) {
                PantallaAdmin2 pantallaAdmin = new PantallaAdmin2(control, usu);
                pantallaAdmin.setVisible(true);
                pantallaAdmin.setLocationRelativeTo(null);
                
                this.dispose();
            }
            
            if(usu.getRol().equals("cliente")) {
                PantallaCliente2 pantallaCliente = new PantallaCliente2(control, usu);
                pantallaCliente.setVisible(true);
                pantallaCliente.setLocationRelativeTo(null);
                
                this.dispose();
            }
            
        } else {
            txtError.setText("Usuario o contraseña incorrecto");
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

